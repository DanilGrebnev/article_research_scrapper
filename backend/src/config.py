import os

# URL удалённого Chrome (Selenium Grid), берётся из переменной окружения или localhost
CHROME_URL = os.getenv("CHROME_URL", "http://localhost:4444/wd/hub")

# Папка для скачанных файлов
DOWNLOAD_DIR = os.path.join(os.path.dirname(os.path.dirname(__file__)), "downloads")

# Запускать Chrome без GUI (True — headless, False — с окном)
HEADLESS = True

# =============================================
# НАСТРОЙКИ СКОРОСТИ СКРАПИНГА (все значения в секундах)
# =============================================
#
# Чем меньше значения — тем быстрее скрапинг,
# но выше риск блокировки со стороны Springer.
# =============================================

# Общий таймаут Selenium: если любой элемент не появился за это время — ошибка.
# Влияет на все find_element вызовы по умолчанию.
WAIT_TIMEOUT = 15

# Максимум ожидания при открытии страницы поиска Springer.
# Если за это время список статей не появился — страница считается сломанной.
PAGE_LOAD_TIMEOUT = 25

# Максимум ожидания при загрузке абстракта конкретной статьи
# (кнопка "Глубокий анализ" на карточке).
ABSTRACT_LOAD_TIMEOUT = 20

# Принудительная пауза между скрапингом каждой страницы.
# Нужна чтобы Springer не заблокировал как бота.
# 0 — без пауз (быстро, но рискованно), 2-3 — безопасно.
DELAY_BETWEEN_PAGES = 2

# Пауза после нажатия "Accept cookies" на баннере Springer.
# Можно уменьшить до 0.5 или убрать (0).
DELAY_AFTER_COOKIE = 1


# =============================================
# ПОВТОРНЫЕ ПОПЫТКИ ПРИ ОШИБКАХ
# =============================================

# Сколько раз пытаться загрузить страницу, если она не открылась.
# 0 — без повторов (одна попытка), 3 — до 4 попыток всего.
PAGE_LOAD_RETRIES = 3

# Пауза перед повторной попыткой: RETRY_DELAY_BASE + номер_попытки * RETRY_DELAY_STEP.
# Попытка 1: 5с, попытка 2: 8с, попытка 3: 11с.
RETRY_DELAY_BASE = 5
RETRY_DELAY_STEP = 3

# Пауза при обнаружении бот-защиты Springer ("Client Challenge").
# Попытка 1: 8с, попытка 2: 12с, попытка 3: 16с.
# Если часто срабатывает — увеличьте CHALLENGE_DELAY_BASE.
CHALLENGE_DELAY_BASE = 8
CHALLENGE_DELAY_STEP = 4


# =============================================
# ПОДКЛЮЧЕНИЕ К CHROME (Docker)
# =============================================

# Сколько раз пытаться подключиться к Chrome контейнеру при запуске.
# Chrome может не сразу стартовать в Docker.
BROWSER_CONNECT_RETRIES = 5

# Пауза между попытками подключения к Chrome (сек).
BROWSER_CONNECT_DELAY = 3
